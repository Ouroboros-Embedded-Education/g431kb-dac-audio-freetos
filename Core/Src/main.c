/** Functions to reproduce the audio **/

void _start_dac(uint8_t *buffer, uint32_t len){
	HAL_DAC_Start_DMA(&hdac1, DAC_CHANNEL_1, (uint32_t*)buffer, len, DAC_ALIGN_8B_R);
}

/** Task to Reproduce audio **/

void vTaskAudioPlayer(void *pvParams){
	audio_play_cmd_t Audio;

	HAL_TIM_Base_Start_IT(&htim6);
	while (1){
		xQueueReceive(xAudioQueue, &Audio, portMAX_DELAY);
		_start_dac(Audio.pAudioData, Audio.u32Len);
		xSemaphoreTake(xDacSem, portMAX_DELAY);
		vPortFree(Audio.pAudioData);
	}
}

/* Auxiliary Functions */

void _apply_volume(uint8_t *buffer, uint32_t len, uint8_t level){
	uint32_t i;
	uint32_t a;
	int32_t v;

	for (i=0 ; i<len ; i++){
		v = buffer[i] - 127;
		a = (v * level) / 100;
		buffer[i] = (uint8_t)(a + 127);
	}
}

void _play_audio(uint8_t *pAudio, uint32_t len){
	uint32_t played, sizeToPlay, iterations;
	uint8_t *pBuffer;
	audio_play_cmd_t AudioPlay;
	bool AudioTaskStarted = false;

	played = 0;

	vTaskSuspend(xAudioTask);
	// we need to skip the 44 bytes header of the wav file
	// check http://soundfile.sapp.org/doc/WaveFormat/
	played += WAV_FILE_HEADER_SIZE;

	iterations = 0;
	while (played < len){
		if ((len - played) > 256){
			sizeToPlay = 256;
		}
		else{
			sizeToPlay = (len - played);
		}

		pBuffer = pvPortMalloc(sizeToPlay);
		memcpy(pBuffer, &pAudio[played], sizeToPlay);
		_apply_volume(pBuffer, sizeToPlay, 8);

		AudioPlay.pAudioData = pBuffer;
		AudioPlay.u32Len = sizeToPlay;
		xQueueSend(xAudioQueue, &AudioPlay, portMAX_DELAY);

		played += sizeToPlay;
		iterations++;
		if (iterations > 2 && AudioTaskStarted == false){
			AudioTaskStarted = true;
			vTaskResume(xAudioTask);
		}
	}

}

void _play_lofi_sample(){
	__HAL_TIM_SET_AUTORELOAD(&htim6, 10624);
	_play_audio((uint8_t*)lofi_sample_wav, lofi_sample_wav_len);
}

void _play_open_the_can(){
	__HAL_TIM_SET_AUTORELOAD(&htim6, 3541);
	_play_audio((uint8_t*)open_the_can_wav, open_the_can_wav_len);
}

void _play_keyboard(){
	__HAL_TIM_SET_AUTORELOAD(&htim6, 1770);
	_play_audio((uint8_t*)keyboard_wav, keyboard_wav_len);
}

/** Start Task */
void vTaskStart (void *pvParams){
	vTaskDelay(pdMS_TO_TICKS(1000));
	_play_lofi_sample();
	vTaskDelay(pdMS_TO_TICKS(250));
	_play_open_the_can();
	while(1){
		vTaskDelay(pdMS_TO_TICKS(1000));
	}
}
